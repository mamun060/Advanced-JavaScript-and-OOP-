// Math floor return integer value
const raju = Math.floor(55.1456456);
console.log(raju);

// Math round return integer value
const popi = Math.round(1000.6565);
console.log(popi);

// Math random return rendom value
const a = Math.random(212.323);
console.log(a);

// Math ceil return it's up nearest value
const b = Math.ceil(1225.2563);
console.log(b);

// Math.trunc(x) returns the integer part of x
const c = Math.trunc(5444.00052145);
console.log(c);

// Math.sign(x) returns if x is negative, null or positive
const d = Math.sign(-1);
console.log(d);

//Math.pow(x, y) returns the value of x to the power of y
const power = Math.pow(4, 2);
console.log(power);

// Math.sqrt(x) returns the square root of x
const squer = Math.sqrt(16);
console.log(squer);

// Math.abs(x) returns the absolute (positive) value of x
const abss = Math.abs(-454.2122);
console.log(abss);

// Math.max return max value
const e = Math.max(100,2,5,8,5);
console.log(e);

// Math.min return minimum value
const g = Math.min(5,6,5,8,7,22);
console.log(g);